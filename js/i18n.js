// ── Core Functions ───────────────────────────────────

export function getLang() {
  return localStorage.getItem('pawdee_lang') || 'th';
}

export function setLang(lang) {
  localStorage.setItem('pawdee_lang', lang);
  applyTranslations(lang);
  updateLangButtons(lang);
}

export function t(key) {
  const lang = getLang();
  return PAWDEE_TRANSLATIONS[lang]?.[key]
      || PAWDEE_TRANSLATIONS['th']?.[key]
      || key;
}

export function applyTranslations(lang) {
  const dict = PAWDEE_TRANSLATIONS[lang] || PAWDEE_TRANSLATIONS['th'];

  // text content
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) el.textContent = dict[key];
  });

  // innerHTML (for elements with HTML tags)
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (dict[key] !== undefined) el.innerHTML = dict[key];
  });

  // placeholder
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (dict[key] !== undefined) el.placeholder = dict[key];
  });

  // aria-label
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (dict[key] !== undefined) el.setAttribute('aria-label', dict[key]);
  });

  // Update html lang attribute
  document.documentElement.lang = lang === 'zh' ? 'zh-TW' : lang;
}

export function updateLangButtons(lang) {
  document.querySelectorAll('.lang-btn').forEach(btn => {
    const active = btn.dataset.lang === lang;
    btn.className = active
      ? 'lang-btn px-2 py-1 text-xs font-bold rounded-lg bg-brand-orange text-white'
      : 'lang-btn px-2 py-1 text-xs font-semibold rounded-lg text-gray-500 hover:bg-orange-50 transition-colors';
  });
}

export function initI18n() {
  const lang = getLang();
  applyTranslations(lang);
  updateLangButtons(lang);

  // Language button click handlers
  document.addEventListener('click', e => {
    const btn = e.target.closest('.lang-btn');
    if (btn?.dataset.lang) setLang(btn.dataset.lang);
  });
}

